            <br/>
            <h2>About the FIRST Robotics Competition</h2>

			<p>The FIRST Robotics Competition is an international high school robotics competition operated by FIRST, an international youth organization created to inspire young people's interest in science and technology.</p>

			<p>Each year, FIRST robotics teams build robots that will compete in competitions. The game changes every year to keep the excitement fresh. The game details are announced at the beginning of January and teams have 6 weeks to build a robot.</p>

            <blockquote class="font-normal">
            <p>Dubbed a varsity Sport for the Mind,&trade;(FRC) combines the excitement of sport with the rigors of science and technology. Under strict rules, limited resources, and time limits, teams of 25 students or more are challenged to raise funds, design a team "brand," hone teamwork skills, and build and program a robot to perform prescribed tasks against a field of competitors. It's as close to "real world" engineering as a student can get. Professional Mentors volunteer their time and talents to guide each team.</p>

            <br/>
            <br/>
            <h3>Students get to:</h3>
            <ul>
                <li>Work alongside professional engineers.
                <li>Build and compete with a robot of their own design.
                <li>Learn and use sophisticated hardware and software.
                <li>Develop design, project management, programming, teamwork, and strategic thinking skills.
                <li>Earn a place in the Championship.
                <li>Qualify for over $16 million in college scholarships.
            </ul>

            <cite><a href="http://www.usfirst.org/">- USFIRST</a></cite>
            </blockquote>
